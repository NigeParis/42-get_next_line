#include "get_next_line.h"
#include <stdio.h>
#include <fcntl.h>
#include <unistd.h>
#include <stdlib.h>

char	*get_next_line(int fd)
{
	
	char *ptr;

	ptr = malloc(5 * sizeof(char));
	if (!ptr)
		return (0);


	read(fd, ptr, 5); 

	return (ptr);

}
